    // target moving
    private void moveObj(){ 
        int upperbound = 500;
        x = rand.nextInt(xhigh-xlow) + xlow;
        y = rand.nextInt(high-low) + low;

        this.remove(jLabel1);
        this.revalidate();
        this.repaint();
        jLabel1.setLocation(x, y);
        this.add(jPanel1);
        
    }
