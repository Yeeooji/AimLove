    //display timer
    public void disp(){
        if (num==10){
            jTextField2.setForeground(Color.yellow);
        }
        if(num==5){
            jTextField2.setForeground(Color.red);
        }
        if(num == 0) {
            jTextField2.setForeground(Color.yellow);      
            jLabel1.setVisible(false);
            jPlay.setEnabled(true);
            timer.cancel();                                                             // To stop timer
        }
        jTextField2.setText("TIMER: "+num);
        
    }
    
    //Function for sound
    private void sound(){
        try {
            File sound = new File("src\\Music\\pop.wav");                               //change directory
            AudioInputStream audioStream = AudioSystem.getAudioInputStream(sound);
            Clip c = AudioSystem.getClip();
            c.open(audioStream);                                                        // opens the file
            c.start();                                                                  // To play the sound
        }
        catch(Exception e){
        }
    }
    public void dataTrans() throws IOException{
        FileWriter fw= new FileWriter("data.txt");
        percent = count/pressed;
        percent = Math.round(percent * 100.0) / 100.0;
        fw.write(count+"\n");
        fw.write(percent+"\n");
        fw.close();
    }
    
